export function getEcosystemCatalogSummary() {
  const categories = new Map<string, number>();
  const channels = new Map<string, number>();
  const trust = new Map<string, number>();
  ecosystemCatalogApps.forEach((app) => {
    categories.set(app.category, (categories.get(app.category) ?? 0) + 1);
    channels.set(app.channel, (channels.get(app.channel) ?? 0) + 1);
    trust.set(app.trust, (trust.get(app.trust) ?? 0) + 1);
  });
  return {
    appCount: ecosystemCatalogApps.length,
    publisherCount: ecosystemPublishers.length,
    extensionPointCount: ecosystemExtensionPoints.length,
    bundleCount: ecosystemBundles.length,
    certificationCount: ecosystemCertifications.length,
    categories: Array.from(categories.entries()),
    channels: Array.from(channels.entries()),
    trust: Array.from(trust.entries()),
    topApps: ecosystemCatalogApps.slice(0, 12),
    featuredBundles: ecosystemBundles.slice(0, 8),
    certificationGates: ecosystemCertifications.slice(0, 6)
  };
}

export function searchEcosystemCatalog(query: string) {
  const normalized = query.trim().toLowerCase();
  if (!normalized) return ecosystemCatalogApps.slice(0, 24);
  return ecosystemCatalogApps
    .filter((app) => `${app.name} ${app.category} ${app.publisher} ${app.channel} ${app.trust} ${app.tags.join(" ")}`.toLowerCase().includes(normalized))
    .slice(0, 48);
}

export function getEcosystemReadinessScore() {
  const verified = ecosystemCatalogApps.filter((app) => app.trust === "verified").length;
  const stable = ecosystemCatalogApps.filter((app) => app.channel === "stable").length;
  const partnerPublishers = ecosystemPublishers.filter((publisher) => publisher.supportTier === "partner").length;
  return Math.round(((verified / ecosystemCatalogApps.length) * 45) + ((stable / ecosystemCatalogApps.length) * 35) + ((partnerPublishers / ecosystemPublishers.length) * 20));
}

